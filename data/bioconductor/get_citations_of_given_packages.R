packages <- BiocManager::available()
packages_vector = strsplit(packages, " ")
conn <- file("input.tmp",open="r")
name <- trimws(readLines(conn)[1], which = c("both"), whitespace = "[ \t\r\n]")
print(name)
if (!requireNamespace(name, quietly = TRUE))
  BiocManager::install(name)
capture.output(citation(name), file = paste(name, ".citations.txt", sep = ""))
capture.output(packageDescription(name), file = paste(name, ".description.txt", sep = ""))
close(conn)
